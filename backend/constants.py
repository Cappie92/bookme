"""
Константы для биллинга и подписок.
MVP: только фиксированные длительности в днях (не календарные месяцы).
"""
# Длительности пакетов подписки: 1/3/6/12 месяцев → 30/90/180/360 дней
DURATION_DAYS = {
    1: 30,
    3: 90,
    6: 180,
    12: 360,
}

DAYS_PER_MONTH = 30  # используется для обратной совместимости; предпочитать DURATION_DAYS

def duration_months_to_days(months: int) -> int:
    """Вернуть длительность в днях для пакета 1/3/6/12 месяцев."""
    return DURATION_DAYS.get(months, months * DAYS_PER_MONTH)
