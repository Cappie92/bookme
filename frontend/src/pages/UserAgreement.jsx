function splitHeadingAndBody(raw) {
  const trimmed = raw.trim()
  const nl = trimmed.indexOf('\n')
  if (nl === -1) return { heading: trimmed, body: '' }
  return {
    heading: trimmed.slice(0, nl).trim(),
    body: trimmed.slice(nl + 1).trim(),
  }
}

